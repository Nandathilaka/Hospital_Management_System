    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int x = Integer.parseInt(txtSalaryFull.getText().toString());  //Calculate the salary
        int y = Integer.parseInt(txtSalaryHalf.getText().toString());
        int z = Integer.parseInt(txtSalaryNull.getText().toString());
        int p = Integer.parseInt(txtUnitFull.getText().toString());
        int q = Integer.parseInt(txtUnitHalf.getText().toString());
        int r = Integer.parseInt(txtUnitNull.getText().toString());

        int Full = x * p;
        int Half = y * q;
        int Null, Sum;
        if (z > 8) {
            Null = (z - 8) * r;
            Sum = Full + Half - Null;
            txtSalaryBalance.setText(Integer.toString(Sum));
            JOptionPane.showMessageDialog(null, "Successfull Salary Calculate");
            return;

        } else {
            Sum = Full + Half;
            txtSalaryBalance.setText(Integer.toString(Sum));
            JOptionPane.showMessageDialog(null, "Successfull Salary Calculate");
        }


    }//GEN-LAST:event_jButton3ActionPerformed

