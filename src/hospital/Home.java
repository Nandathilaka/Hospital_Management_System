    private void btnSalarySearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalarySearchActionPerformed
        Statement ss = DataBaseHospitalMS.getStatement();  //Connect to the database
        String EmpID = txtSalaryEmployeeID.getText(); //Collecting the input
        try {
            if (txtSalaryEmployeeID.getText().length() == 0) {  //Check Empty Fields
                JOptionPane.showMessageDialog(null, "Please Fill Up Employee ID", "WARNING", JOptionPane.WARNING_MESSAGE);
            } else if (txtFromeDate.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, "Please Fill Up From Date", "WARNING", JOptionPane.WARNING_MESSAGE);
            } else if (txtToDate.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, "Please Fill Up To Date", "WARNING", JOptionPane.WARNING_MESSAGE);
            } else if (ValidateSearchEmployee.validate_searchEmp(EmpID)) { //Check validate Employee ID
                ResultSet rs = ss.executeQuery("SELECT COUNT(Attendance) FROM attendance WHERE EmployeeID='" + txtSalaryEmployeeID.getText() + "' AND Attendance='Full Day' AND Date between '" + txtFromeDate.getText() + "' AND '" + txtToDate.getText() + "' ");
                while (rs.next()) {
                    txtSalaryFull.setText(rs.getString("COUNT(Attendance)"));
                }
                ResultSet rs1 = ss.executeQuery("SELECT COUNT(Attendance) FROM attendance WHERE EmployeeID='" + txtSalaryEmployeeID.getText() + "' AND Attendance='Half Day' AND Date between '" + txtFromeDate.getText() + "' AND '" + txtToDate.getText() + "'");
                while (rs1.next()) {
                    txtSalaryHalf.setText(rs1.getString("COUNT(Attendance)"));
                }
                ResultSet rs2 = ss.executeQuery("SELECT COUNT(Attendance) FROM attendance WHERE EmployeeID='" + txtSalaryEmployeeID.getText() + "' AND Attendance='Null' AND Date between '" + txtFromeDate.getText() + "' AND '" + txtToDate.getText() + "' ");
                while (rs2.next()) {
                    txtSalaryNull.setText(rs2.getString("COUNT(Attendance)"));
                    JOptionPane.showMessageDialog(null, "Success Search Attendance");
                }

            } else {
                JOptionPane.showMessageDialog(null, "Incorrect Employee ID\nPlease Check Whether Employee ID.", "ERROR", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSalarySearchActionPerformed

